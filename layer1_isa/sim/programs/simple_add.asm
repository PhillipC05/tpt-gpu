;; This program tests basic ALU and memory operations:
;; 1. Load two values from data memory
;; 2. Add them together
;; 3. Store the result back to data memory
;; 4. Loop indefinitely
;;
;; Memory layout:
;;   data_mem[0] = 0   (initially)
;;   data_mem[1] = 10
;;   data_mem[2] = 20
;;   data_mem[3] = 30
;;   data_mem[4] = 0   (result location)
;;   data_mem[5] = 0   (result location)
;;============================================================================

.org 0x00000000

;;--------------------------------------------------------------------------
;; Test 1: Simple integer addition
;; Load values from data memory, add, store result
;;--------------------------------------------------------------------------
start:
    LW    R1, R0, 0       ; R1 = data_mem[0] = 0
    LW    R2, R0, 4       ; R2 = data_mem[1] = 10
    LW    R3, R0, 8       ; R3 = data_mem[2] = 20
    LW    R4, R0, 12      ; R4 = data_mem[3] = 30

    ADD   R5, R2, R3      ; R5 = 10 + 20 = 30
    ADD   R6, R4, R5      ; R6 = 30 + 30 = 60
    ADD   R7, R6, R2      ; R7 = 60 + 10 = 70
    ADD   R8, R7, R3      ; R8 = 70 + 20 = 90
    ADD   R8, R8, R2      ; R8 = 90 + 10 = 100
    ADD   R9, R8, R4      ; R9 = 100 + 30 = 130

    SUB   R10, R9, R2     ; R10 = 130 - 10 = 120

    SW    R0, R10, 16     ; data_mem[4] = 120
    SW    R0, R8, 20      ; data_mem[5] = 100

;;--------------------------------------------------------------------------
;; Test 2: Bitwise operations
;;--------------------------------------------------------------------------
    LW    R11, R0, 4      ; R11 = 10 (0x0000000A)
    LW    R12, R0, 8      ; R12 = 20 (0x00000014)

    AND   R13, R11, R12   ; R13 = 10 & 20 = 0
    OR    R14, R11, R12   ; R14 = 10 | 20 = 30 (0x1E)
    XOR   R15, R11, R12   ; R15 = 10 ^ 20 = 30 (0x1E)
    SLL   R16, R11, R12   ; R16 = 10 << 20 = 0x0A000000
    SRL   R17, R16, R12   ; R17 = 0x0A000000 >> 20 = 10

;;--------------------------------------------------------------------------
;; Test 3: Immediate operations
;;--------------------------------------------------------------------------
    ADDI  R18, R0, 42     ; R18 = 42
    ADDI  R19, R18, 8     ; R19 = 50
    SUBI  R20, R19, 20    ; R20 = 30

;;--------------------------------------------------------------------------
;; Test 4: Multiplication
;;--------------------------------------------------------------------------
    MUL   R21, R2, R3     ; R21 = 10 * 20 = 200

;;--------------------------------------------------------------------------
;; Test 5: Final store for verification
;;--------------------------------------------------------------------------
    SW    R0, R10, 16     ; data_mem[4] = 120
    SW    R0, R8, 20      ; data_mem[5] = 100
    SW    R0, R21, 24     ; data_mem[6] = 200

;;--------------------------------------------------------------------------
;; Infinite loop
;;--------------------------------------------------------------------------
done:
    BEQ   R0, R0, done    ; Branch to self

